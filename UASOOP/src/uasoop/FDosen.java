    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String NPWP = this.tNpwp.getText();
        String NAMA = this.tNama.getText();
        String JEN = this.tJenjang.getSelectedItem().toString();
        String JKEL = "L";
        if(this.JkP.isSelected()){
            JKEL = "P";
        }
        String SQLUpdate = "UPDATE dosen SET Nama='"+NAMA+"', Jenjang='"+JEN+"', Jk='"+JKEL+"' WHERE Npwp='"+NPWP+"';";
        if(this.updatedata(SQLUpdate)){
            this.getdata();
            JOptionPane.showMessageDialog(null, "Data telah di Update");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

