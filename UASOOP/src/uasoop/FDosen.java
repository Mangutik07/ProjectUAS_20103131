    private void tbDosenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDosenMouseClicked
        this.tNpwp.setText( model.getValueAt(tbDosen.getSelectedRow(), 0).toString());
        this.tNama.setText( model.getValueAt(tbDosen.getSelectedRow(), 1).toString());
        this.tJenjang.setSelectedItem(model.getValueAt(tbDosen.getSelectedRow(), 2).toString());
        String txJK = model.getValueAt(tbDosen.getSelectedRow(), 3).toString();
        if(txJK.equals("Perempuan")){
            this.JkP.setSelected(true);
        }else{
            this.JkL.setSelected(true);
        }
        
        this.lbSub.setText("Edit Data "+this.tNama.getText());
        this.btnSimpan.setEnabled(false);
        this.btnUpdate.setEnabled(true);
        this.btnDelete.setEnabled(true);
    }//GEN-LAST:event_tbDosenMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String NPWP = this.tNpwp.getText();
        String NAMA = this.tNama.getText();
        
        int opsi = JOptionPane.showConfirmDialog(null,"Yakin akan menghapus data dari "+NAMA+"("+NPWP+")?","Konfirmasi", JOptionPane.YES_NO_OPTION );
        if(opsi == JOptionPane.YES_OPTION){
            String SQLDelete = "DELETE FROM dosen WHERE Npwp='"+NPWP+"';";
            if(this.updatedata(SQLDelete)){
                this.getdata();
                JOptionPane.showMessageDialog(null, "Data telah dihapus");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

