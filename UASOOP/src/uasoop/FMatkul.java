    private void tbMatkulMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMatkulMouseClicked
        this.tKode.setText( model.getValueAt(tbMatkul.getSelectedRow(), 0).toString());
        this.tNama.setText( model.getValueAt(tbMatkul.getSelectedRow(), 1).toString());
        this.tSemester.setSelectedItem(model.getValueAt(tbMatkul.getSelectedRow(), 2).toString());
        String Jen = model.getValueAt(tbMatkul.getSelectedRow(), 3).toString();
        if(Jen.equals("Pilihan")){
            this.JenP.setSelected(true);
        }else{
            this.JenW.setSelected(true);
        }
        
        this.lbSub.setText("Edit Data "+this.tNama.getText());
        this.btnSimpan.setEnabled(false);
        this.btnUpdate.setEnabled(true);
        this.btnDelete.setEnabled(true);
    }//GEN-LAST:event_tbMatkulMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String KOD = this.tKode.getText();
        String NAMA = this.tNama.getText();
        
        int opsi = JOptionPane.showConfirmDialog(null,"Yakin akan menghapus data dari "+NAMA+"("+KOD+")?","Konfirmasi", JOptionPane.YES_NO_OPTION );
        if(opsi == JOptionPane.YES_OPTION){
            String SQLDelete = "DELETE FROM matkul WHERE Kode='"+KOD+"';";
            if(this.updatedata(SQLDelete)){
                this.getdata();
                JOptionPane.showMessageDialog(null, "Data telah dihapus");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

