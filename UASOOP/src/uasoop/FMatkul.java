    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String KOD = this.tKode.getText();
        String NAMA = this.tNama.getText();
        String SEM = this.tSemester.getSelectedItem().toString();
        String JEN = "W";
        if(this.JenP.isSelected()){
            JEN = "P";
        }
        String SQLUpdate = "UPDATE matkul SET Nama='"+NAMA+"', Semester='"+SEM+"', Jenis='"+JEN+"' WHERE Kode='"+KOD+"';";
        if(this.updatedata(SQLUpdate)){
            this.getdata();
            JOptionPane.showMessageDialog(null, "Data telah di Update");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

