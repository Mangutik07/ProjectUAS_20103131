    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String NIM = this.tNim.getText();
        String NAMA = this.tNama.getText();
        String TGL = this.tTgl.getText();
        String JUR = this.tJurusan.getSelectedItem().toString();
        String JKEL = "L";
        if(this.JkP.isSelected()){
            JKEL = "P";
        }
        String SQLUpdate = "UPDATE mhs SET Nama='"+NAMA+"', Jurusan='"+JUR+"', Jk='"+JKEL+"', Tgl='"+TGL+"' WHERE Nim='"+NIM+"';";
        if(this.updatedata(SQLUpdate)){
            this.getdata();
            JOptionPane.showMessageDialog(null, "Data telah di Update");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

