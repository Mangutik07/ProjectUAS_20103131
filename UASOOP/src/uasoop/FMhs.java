    private void TbMhsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TbMhsMouseClicked
        this.tNim.setText( model.getValueAt(TbMhs.getSelectedRow(), 0).toString());
        this.tNama.setText( model.getValueAt(TbMhs.getSelectedRow(), 1).toString());
        this.tTgl.setText( model.getValueAt(TbMhs.getSelectedRow(), 2).toString());
        this.tJurusan.setSelectedItem(model.getValueAt(TbMhs.getSelectedRow(), 3).toString());
        String txJK = model.getValueAt(TbMhs.getSelectedRow(), 4).toString();
        if(txJK.equals("Perempuan")){
            this.JkP.setSelected(true);
        }else{
            this.JkL.setSelected(true);
        }
        
        this.lbSub.setText("Edit Data "+this.tNama.getText());
        this.btnSimpan.setEnabled(false);
        this.btnUpdate.setEnabled(true);
        this.btnDelete.setEnabled(true);
    }//GEN-LAST:event_TbMhsMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String NIM = this.tNim.getText();
        String NAMA = this.tNama.getText();
        
        int opsi = JOptionPane.showConfirmDialog(null,"Yakin akan menghapus data dari "+NAMA+"("+NIM+")?","Konfirmasi", JOptionPane.YES_NO_OPTION );
        if(opsi == JOptionPane.YES_OPTION){
            String SQLDelete = "DELETE FROM mhs WHERE Nim='"+NIM+"';";
            if(this.updatedata(SQLDelete)){
                this.getdata();
                JOptionPane.showMessageDialog(null, "Data telah dihapus");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

